#include "parse/parse.h"

void		free_arg(char **arg)
{
	int i;

	i = 0;
	if (arg == NULL)
		return ;
	while (arg[i])
	{
		ft_strdel(&arg[i]);
		i++;
	}
	free(arg);
}
